// Calculator class with simple operations
public class Calculator {
	// sums 2 numbers
	public int sum(int n1, int n2) {
		return n1 + n2;
	}

	// multiplies 2 numbers
	public int multiply(int n1, int n2) {
		return n1 * n2;
	}

	// rest 2 numbers
	public int rest(int n1, int n2) {
		return n1 - n2;
	}

	// divide 2 numbers
	public int divide(int n1, int n2) {
		return n1 / n2;
	}
	
	// divide 2 numbers
	public int percentage(int n1, int n2) {
		return n1 / n2;
	}
}